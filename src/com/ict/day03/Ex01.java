package com.ict.day03;

public class Ex01 {
	public static void main(String[] args) {
		
				// 증감연산사 : 1 증가 1 감소 하는 연산자
				// ++ / -- 변수 : 현재 변수가 가지고 있는 값에 1을 증가/감소 하고 나머지를 실행
				// 변수 ++/-- : 현재변수를 가지고 실행한 후 그 명령 구문이 끝날때 1증가/감소);
				
				int su1 = 10;
				int su2 = 10;
				System.out.println(++su1);
				System.out.println(su2++);
				System.out.println(su1);
				System.out.println(su2);
	}
}
